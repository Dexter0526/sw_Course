package com.exam;

public class Ex2 {
	public static void main(String[] args) {
		//int a = 10, int b= 20;
		int a = 10;
		int b = 20;
		
		// 출력문 타입
		System.out.print("a변수 출력\n");
		System.out.println("a = " + a);
		System.out.printf("a = %d\n", a);
		// %d 정수, %f 실수, %c 문자, %s 문자열
		
		System.out.println("a="+a+", b="+b);
		System.out.printf("a=%d, b=%d\n",a , b);
	}
}
